print(2 + 3)
print(5 - 3)
print(2 * 3)
print(7 / 2)
# // floor division - It divides two numbers and rounds down
print(7 // 2)
#  remainder - The left over part after division - used to check if a number is even
print(10 % 2)
#  ** exponentiation - It raises a number to the power of another number
print(2 ** 3)

# shortcuts
''' shorthand assignment
    syntax : <operator>=
    - apply an operator and reassign the result in one step 
'''
x = 3
y = x+2
x += 2
print(x)
print(y)
